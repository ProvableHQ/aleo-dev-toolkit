// Helper function to convert JSON image format to 2D array
const convertJsonImageTo2D = (jsonImage) => {
  return jsonImage.map(row => {
    // Convert object with numbered keys to array
    return Object.keys(row).sort((a, b) => parseInt(a) - parseInt(b)).map(key => row[key]);
  });
};

